exports("Exit",function(Coords)
	for Class,Levels in pairs(Interiors) do
		for Level,Value in pairs(Levels) do
			if #(Coords - Value["Exit"]) <= 0.75 then
				return { Class = Class, Level = Level }
			end
		end
	end
end)